    private void loadCopies() {

    try {

        Connection con = DB_connect.getConnection();

        String sql = "SELECT copy_id, book_id, acquisition_number, status, date_received "
                   + "FROM book_copy WHERE book_id = ?";

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, bookid);

        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) tblCopies.getModel();
        model.setRowCount(0); // clear table
        
        while (rs.next()) {
            java.sql.Date sqlDate = rs.getDate("date_received");
             String formatDate = "";
            if(sqlDate != null){
                java.text.SimpleDateFormat sdf =  new java.text.SimpleDateFormat("yyyy-MM-dd");
                formatDate =  sdf.format(sqlDate);
            }
            model.addRow(new Object[]{
                rs.getInt("copy_id"),
                rs.getInt("book_id"),
                rs.getString("acquisition_number"),
                rs.getString("status"),
                formatDate
            });

        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
