    private void populateTable(){
        int colCount;
        
        try{
            
            Connection cn = DB_connect.getConnection();
        Statement st = cn.createStatement();
        ResultSet res = st.executeQuery("SELECT * FROM category");

        DefaultTableModel tblModel = (DefaultTableModel) tblCategory.getModel();
        tblModel.setRowCount(0);

        while (res.next()) {
            Vector<Object> ColData = new Vector<>();
            ColData.add(res.getInt("category_id"));
             ColData.add(res.getString("category_name"));
            
           

            tblModel.addRow(ColData);
        }
            
        } catch (SQLException ex) {
            System.getLogger(UserMaintenanceForm.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        
    }
