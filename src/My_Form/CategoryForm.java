    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String CategoryName = txtSearch.getText();
       int colCount;
        
        try{
            
           Connection cn = DB_connect.getConnection();
           Statement st = cn.createStatement();
           String query = "Select * FROM category WHERE category_name LIKE '%" + CategoryName+"%'";
           ResultSet res = st.executeQuery(query);
           ResultSetMetaData rsData = res.getMetaData();
           colCount = rsData.getColumnCount(); 
           
            DefaultTableModel tblModel = (DefaultTableModel)tblCategory.getModel();
            tblModel.setRowCount(0);
           while(res.next()){
               
               Vector<Object> ColData = new Vector<>();

            ColData.add(res.getInt("category_id"));
            ColData.add(res.getString("category_name"));
           

            tblModel.addRow(ColData);
            }
            
        } catch (SQLException ex) {
            System.getLogger(UserMaintenanceForm.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        } 
    }//GEN-LAST:event_txtSearchKeyReleased

